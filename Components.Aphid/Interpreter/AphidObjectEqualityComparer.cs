using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Components.Aphid.Interpreter
{
    public class AphidObjectEqualityComparer : IEqualityComparer<AphidObject>
    {
        public bool Equals(AphidObject x, AphidObject y)
        {
            return OperatorHelper.EqualsCore(x, y);
        }

        public int GetHashCode(AphidObject obj)
        {
            return obj.Value.GetHashCode();
        }
    }
}
